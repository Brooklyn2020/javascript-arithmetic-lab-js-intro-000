function add(a,b) {
return a + b
}
function subtract(a,b) {
  a - b
}
function multiply(a,b) {
  return a * b
}
function divide(a,b) {
  return a / b
  }

function increment(n) {
  n++;
  return n
}

function decrement(n) {
  n--;
  return n
}

function makeInt(string) {
  parseInt("2", 10);
  return string
}

function preserveDecimal(string) {
  parseFloat("2.22222",10);
  return string
}
